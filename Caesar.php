description: >-
  Cäsar stammt aus einer der ältesten Adelsfamilien Roms, die sich über Aeneas und Anchises von Venus herleitet. Nach einer für ihn sehr einträglichen Statthalterschaft in Spanien bringt er sich in ein Bündnis von Geld (Crassus) und Macht (Pompeius) ein und gründet das erste Triumvirat. Durch Einschüchterung seines Kollegen regiert er bereits 59 als Konsul wie ein Alleinherrscher. Nach der Statthalterschaft in Gallien (58-52) setzt er sich in einem Bürerkrieg gegen seine republikanischen Gegner durch. Er treibt die Umwandlung Roms in eine Monarchie energisch voran. Im März 44 wird er während einer Senatssitzung unter anderen auch von denen ermordet, die er nach dem Bürgerkrieg begnadigt hatte.
