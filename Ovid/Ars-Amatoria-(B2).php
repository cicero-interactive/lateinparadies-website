        latin: |
          Quid tibi praecipiam teneros quoque mittere versus?
          Ei mihi, non multum carmen honoris habet.
          Carmina laudantur, sed munera magna petuntur:
          Dummodo sit dives, barbarus ipse placet.
          Aurea sunt vere nunc saecula: plurimus auro
          Venit honos: auro conciliatur amor.
          Ipse licet venias Musis comitatus, Homere,
          Si nihil attuleris, ibis, Homere, foras.
          Sunt tamen et doctae, rarissima turba, puellae;
          Altera non doctae turba, sed esse volunt.
          Utraque laudetur per carmina: carmina lector
          Commendet dulci qualiacumque sono;
          His ergo aut illis vigilatum carmen in ipsas
          Forsitan exigui muneris instar erit.
        german: |
          Warum soll ich dir vorschreiben, Liebesbriefe zu schicken?
          Wehe mir, ein Gedicht hat nicht viel Ehre.
          Gedichte werden gelobt, aber große Geschenke sind erwünscht.
          Wenn er nur reich ist, dann würde selbst ein Barbar gefallen.
          Golden sind wahrhaftig jetzt die Zeiten: Die meiste(n) Ehre(nämter)
          verschafft man sich durch Gold, mit Gold wird Liebe verschafft.
          Magst du auch persönlich kommen, Homer, begleitet von den Musen;
          wenn du nichts mitbringst, dann wirst du, Homer, vor die Tür gesetzt werden.
          Dennoch gibt es auch gebildete Mädchen, allerdings nur selten;
          die anderen sind nicht gebildet, aber wollen es sein.
          Beide Arten möchten gelobt werden: Der Vorleser <span class="annotation">[dieses Gedichts]</span> empfiehlt,
          Gedichte jeglicher Art durch gute Betonung / mit süßer Stimme vorzulesen;
          ein Gedicht, das in der Nacht entstanden ist, wird vielleicht wie ein kleines Geschenk sein,
          wenn es auf sie selbst verfasst ist / sie selbst darin vorkommt.
