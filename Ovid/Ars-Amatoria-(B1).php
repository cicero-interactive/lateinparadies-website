          er dichtet wahres. Hilf meinem Unterfangen, Mutter der Liebe <span class="annotation">(Venus)</span>!
          Bleibt fort, zarte Haarbänder, Kennzeichen der Scham,
          und du, langer Saum an der Tunika verheireteter Frauen, der du die Füße in Mitte bedeckst.
          Ich werde sichere Liebe und erlaubte Diebereien <span class="annotation">("das Herz stehlen")</span> besingen,
          und in meinem Gedicht wird nichts Verbotenes sein.
          Am Anfang bemühe dich zu finden, wen du lieben willst,
          der du nun zuerst als Soldat zu neuen Waffen kommst.
          Als nächstes ist es die Aufgabe, das geliebte Mädchen zu erobern;
          und als drittes, dass die Liebe lange Zeit anhält.
          Das ist der Plan. Diese Rennbahn wird mit unserem Wagen befahren,
          diese Wendemarke muss durch den schnellen Wagen gekratzt werden.
          Solange es erlaubt ist und du überall hingehen kannst mit lockeren Zügeln,
          wähle aus, zu welcher du sagst: "Du allein gefällst mir".
          Diese wird dir nicht durch die zarten Lüfte hinabgleiten:
          Deine Augen müssen ein passendes Mädchen suchen.
          Der Jäger weiß gut, wo er den Hirschen die Netze ausspannt;
          er weiß gut, in welchem Tal sich der grimmige Eber aufhält.
          Die Büsche sind dem Vogelfänger wohl bekannt. Einer, der die Angel hinaushält,
          weiß, welche Gewässer von vielen Fischen durchschwommen werden.
          Auch du, der du den Stoff für eine lange Liebe suchst,
          lerne zuerst, an welchem Ort sich Mädchen in großer Zahl aufhalten.
          Ich verlange nicht von dir, der du suchst, in die weite Ferne zu segeln,
          noch, dass du dir, um ein Mädchen zu finden, die Sohlen abläufst.
          Mag Perseus auch Andromeda von den schwarzen Indern herbeigeschleppt haben,
          oder das griechische Mädchen <span class="annotation">(Helena)</span> für Paris geraubt worden sein:
          Rom gibt dir bereits so viele wunderschöne Mädchen, sodass du sagen wirst:
          "Diese Stadt hat, was auch immer es auf der Welt gibt".
          Wie viele Felder Gargara hat, wie viele Trauben Methymna,
          wie viele Fische das Meer, wie viele Vögel das Laub verbirgt,
          wie viele Sterne der Himmel hat: So viele Mädchen hat dein Rom.
          Die Mutter ließ sich in Äneas' Stadt nieder.
