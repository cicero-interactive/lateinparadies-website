          Wenn irgendjemand im Volk die Kunst zu lieben nicht kennt,
          soll er dies lesen und danach als Gelehrter lieben.
          Durch Kunst werden schnelle Schiffe mit Ruder und Segel gelenkt,
          durch Kunst werden leichte Wägen gelenkt: Mit Kunst muss auch die Liebe geleitet werden.
          Automedon war fähig, Wägen zu lenken und starre Zügel handzuhaben,
          und Tiphys war Meister auf dem Haemonischen Hinterdeck:
          Mich hat Venus zum Künstler der zarten Liebe gemacht;
          ich werde der Tiphys und Automedon der Liebe genannt werden.
          Jener <span class="annotation">(Amor = die Liebe)</span> ist zwar wild und mag sich mir oft widersetzen,
          aber er ist noch ein Junge im zarten Lebensalter und bereit gelenkt zu werden.
          Der Sohn der Philyras <span class="annotation">(der Kentaur Cheiron)</span> brachte dem jungen Achill das Lyraspiel bei
          und dämpfte dessen wildes Gemüt mit sanfter Kunst.
          Er, der so oft seine Gefährten und Freunde erschreckte,
          soll sich vor dem alten Greis gefürchtet haben.
          Die Hände, die Hektor dereinst fühlen sollte, streckte jener
          auf Befehl seines Lehrers aus, um Schläge zu erhalten.
          Erzieher des Aecus-Enkels war Cheiron; ich bin Erzieher Armors:
          beides wilde Knaben, beides Söhne einer Göttin.
          Aber auch ein Stier muss seinen Nacken dem Joch des Pfluges beugen,
          und ein feuriges Pferd beißt mit seinen Zähnen auf das Zaumzeug.
