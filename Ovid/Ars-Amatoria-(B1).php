          Wird sie deinen Liebesbrief lesen? Zwinge sie nicht!
          Sieh zu, dass du ihr Komplimente machst.
          Was sie gelesen haben wollte, wird sie auf das Gelesene zurückschreiben wollen:
          Diese Sachen werden der Reihe nach Schritt für Schritt eintreten.
          Vielleicht wird dir zuerst ein trauriger Brief zukommen,
          und sie wird dich bitten, dass du sie nicht in Unruhe versetzen <span class="annotation">(nerven)</span> sollst.
          Was jene erbittet, fürchtet sie; was sie nicht erbittet, wünscht sie, nämlich, dass du hartnäckig bleibst.
          Mach weiter, und du wirst bald das besitzen, was du dir wünscht.
