  Cicero stammte aus einer reichen Ritterfamilie. Von seiner Schulzeit an fiel er durch herausragende Leistungen, galt als der beste Redner seiner Zeit und verfasste Werke über die Rhetorik. Er erreichte jedes politische Amt zum frühesten dafür vorgesehenen Zeitpunkt. Höhepunkt seiner Karriere war die Aufdeckung und Niederschlagung der Catilinarischen Verschwörung, doch tritt seine politische Bedeutung - überschattet von Bürgerkriegen und autoritären Führern wie Sulla, Pompeius und Caesar - hinter seinen Erfolgen als Anwalt und Philosoph zurück. Während und nach seiner Verbannung beschäftigte er sich intensiv mit der Philosophie, die er den Römern verständlich zu machen versuchte. Nach der Ermordung Caesars und der Bildung des Zweiten Triumvirats wurde er auf Befehl von Marcus Antonius ermordet.
