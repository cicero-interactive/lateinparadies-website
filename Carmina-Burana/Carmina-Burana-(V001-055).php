    sections:
      - latin: |
          O Fortuna,
          velut Iuna
          statu variabilis,
          semper crescis
          aut decrescis;
          vita detestabilis
          nunc obdurat
          et tunc curat
          Iudo mentis aciem,
          egestatem,
          potestatem
          dissolvit ut glaciem.
        german: |
          Oh Fortuna,
          wie der Mond
          veränderlich im Zustand,
          immer wächst du
          oder schrumpfst du,
          das abscheuliche Leben
          ist jetzt hart
          und sorgt sich dann
          spielerisch um die Schärfe des Geistes,
          die Armut,
          die Macht
          löst es auf wie Eis.
      - style: verse-seperator
      - latin: |
          Sors inmanis
          et inanis,
          rota tu volubilis,
          status malus,
          vana salus
          semper dissolubilis,
          obumbrata
          et velata
          michi quoque niteris;
          nunc per ludum
          dorsum nudum
          fero tui sceleris.
        german: |
          Grausames und
          eitles Schicksal,
          du rollendes Rad,
          schlechter Zustand,
          vergängliches Heil,
          immer auflösbar,
          in Dunkel gehüllt
          ergreifst du auch mich,
          ich ertrage nun
          durch dein
          verbrecherisches Spiel
          einen nackten Rücken.
      - style: verse-seperator
      - latin: |
          Sors salutis
          et virtutis
          michi nunc contraria,
          est affectus
          et defectus
          semper in angaria.
          Hac in hora
          sine mora
          corde pulsum tangite;
          quod per sortem
          sternit fortem,
          mecum omnes plangite!
        german: |
          Schicksal des Heils
          und der Tugend,
          mir jetzt feindlich,
          ist ein Aufbäumen
          und eine Erschöpfung
          immer im Frondienst.
          In dieser Stunde,
          ohne Verzug,
          zupf die Saite;
          weil durch das Schicksal
          der Starke fällt,
          klagt alle laut mit mir!
