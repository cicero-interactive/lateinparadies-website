  Carmina Burana ist der Name einer Anthologie von 254 mittellateinischen, seltener mittelhochdeutschen, altfranzösischen oder provenzalischen Lied- und Dramentexten, die 1803 in der Bibliothek des Klosters Benediktbeuern gefunden wurde. Die Texte wurden im 11. und 12. Jahrhundert von zumeist anonymen Dichtern verfasst. Die Carmina Burana gelten neben den älteren Carmina Cantabrigiensia als wichtigste Sammlung der Vagantendichtung.
