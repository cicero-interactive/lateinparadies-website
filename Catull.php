description: >-
  Catull stammt aus einer Ritterfamilie aus Gallia Cisalpina. Den Großteil seines Lebens verbrachte er aber in Rom, wo er sich ebenfalls in adeligen Kreisen bewegte. Klagen über seine angebliche Armut sind daher poetische Fiktion. Über sein Leben gibt es nur verstreute, widersprüchliche Angaben, die teilweise auch aus seinen Gedichten stammen: Liebeslyrik (Lesbia), ein Gedicht über den Besuch des Grabes seines Bruders (anlässlich seines Dienstes unter Gaius Memmius in Bithynien), Invektiven (gegen Prominente wie Caesar und Cicero). Im Stil der Neoteriker, zu denen er gehörte, schrieb er auch Gedichte mit mythologischen Inhalten. Er beeinflusste damit auch andere Dichter bis ins Mittelalter.
