  Jacobus de Voragine war Erzbischof und kirchenlateinischer Schriftsteller. Er verfasste die im Spätmittelalter unter Hinzunahme des erbaulichen kirchlichen Schrifttums, genauso wie den im Volkstum bekannten Legendenerzählungen die weitverbreitete Sammlung von Heiligenleben, die sog. Legenda aurea, die große Bedeutung sowohl für die Literatur, als auch für die Bildende Kunst der späteren Jahrhunderte erlangte, wobei Maler und Bildhauer sich bei ihren Darstellungen auf diese wertvolle Quelle in Bezug auf die jeweiligen Szenen und Attribute bezogen. 
