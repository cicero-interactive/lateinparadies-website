													<font size="4" color="#F2F2F2">
														Hac pugna pugnata Romam profectus est nullo resistente. In propinquis urbi montibus moratus est.
														Cum aliquot ibi dies castra habuisset et Capuam reverteretur, Q. Fabius Maximus, dictator Romanus, in agro Falerno ei se obiecit.
														Hic clausus locorum angustiis noctu sine ullo detrimento exercitus se expedivit; Fabioque, callidissimo imperatori, dedit verba. 
														Namque obducta nocte sarmenta in cornibus iuvencorum deligata incendit eiusque generis multitudinem magnam dispalatam immisit. 
														Quo repentino obiecto visu tantum terrorem iniecit exercitui Romanorum, ut egredi extra vallum nemo sit ausus.
														Hanc post rem gestam non ita multis diebus M. Minucium Rufum, magistrum equitum pari ac dictatorem imperio, dolo productum in proelium fugavit.
														Tiberium Sempronium Gracchum, iterum consulem, in Lucanis absens in insidias inductum sustulit. M. Claudium Marcellum, quinquies consulem, apud Venusiam pari modo interfecit.
														Longum est omnia enumerare proelia. Quare hoc unum satis erit dictum, ex quo intellegi possit, quantus ille fuerit: quamdiu in Italia fuit,
														nemo ei in acie restitit, nemo adversus eum post Cannensem pugnam in campo castra posuit.
													</font>
												</p>
											</td>
											<td></td>
											<td>
												<p>
													<font size="4" color="#F2F2F2">
														Nachdem diese Schlacht gekämpft worden war, brach er nach Rom auf, wobei ihm niemand Widerstand leistete.
														In den der Stadt benachbarten Bergen verweilte er.
														Nachdem er dort einige Tage sein Lager aufgeschlagen hatte und nach Capua zurückkehrte,
														stellte sich ihm Q. Fabius Maximus, der römische Diktator, auf dem Falernerfeld entgegen.
														Hannibal wurde eingeschlossen durch einen Engpass,
														aber in der Nacht befreite er sich ohne irgendeinen Verlust des Heeres und und führte Fabius,
														einen raffinierten Feldherrn, hinters Licht. Denn unter dem Schleier der Nacht entzündete er auf den Hörnern der
														Rinder gebundenes Reisig und schickte eine große Anzahl dieser Art überall verstreut los.
														Durch diesen ihnen plötzlich vorgesetzen Anblick befiel so große Furcht das Heer der Römer,
														dass sich niemand außerhalb des Walls wagte.
														Nicht allzu viele Tage nach dieser Tat schlug er Marcus Minucius Rufinus,
														einen Reitergeneral mit gleicher Befehlsgewalt wie der Diktator, den er durch eine List zum Kampf verleitet hatte, in die Flucht.
														Er hat Tiberius Sempronius Gracchus, der zum zweiten mal Konsul war,
														auf dem lukanischen Feld in Hannibals Abwesenheit in eine Falle gelockt und getötet.
														Marcus Claudius Marcellus, der zum fünften Mal Konsul war, tötete er auf dieselbe Weise bei Venusia.
														Es würde zu weit führen, alle Schlachten aufzuzählen.
														Daher wird diese eine Aussage genug sein, aus der man erkennen kann, wie bedeutend jener Mann war:
														Solange er in Italien war, hat ihm niemand in der Schlacht widerstanden, und niemand hat gegen ihn
														nach der Schlacht bei Cannae auf freiem Feld ein Lager aufgeschlagen.
													</font>
												</p>
