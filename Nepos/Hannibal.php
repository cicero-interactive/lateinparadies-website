													<font size="4" color="#F2F2F2">
														Conflixerat apud Rhodanum cum P. Cornelio Scipione consule eumque pepulerat. Cum hoc eodem Clastidi apud Padum decernit sauciumque inde ac fugatum dimittit.
														Tertio idem Scipio cum collega Tiberio Longo apud Trebiam adversus eum venit. Cum his manum conseruit, utrosque profligavit.
														Inde per Ligures Appenninum transiit, petens Etruriam.
														Hoc itinere adeo gravi morbo afficitur oculorum, ut postea numquam dextro aeque bene usus sit. 
														Qua valetudine cum etiam tum premeretur lecticaque ferretur C. Flaminium consulem apud Trasumenum cum exercitu insidiis circumventum occidit neque multo post C. Centenium praetorem cum delecta manu saltus occupantem.
														Hinc in Apuliam pervenit.
														Ibi obviam ei venerunt duo consules, C. Terentius et L. Aemilius. Utriusque exercitus uno proelio fugavit, Paulum consulem occidit et aliquot praeterea consulares,
