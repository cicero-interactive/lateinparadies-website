												<p>
													<font size="4" color="#F2F2F2">
														Hannibal, Hamilcaris filius, Carthaginiensis. Si verum est, quod nemo dubitat, ut populus Romanus omnes gentes virtute superarit,
														non est infitiandum Hannibalem tanto praestitisse ceteros imperatores prudentia, quanto populus Romanus antecedat fortitudine cunctas nationes.
														Nam quotienscumque cum eo congressus est in Italia, semper discessit superior. Quod nisi domi civium suorum invidia debilitatus esset,
														Romanos videtur superare potuisse. Sed multorum obtrectatio devicit unius virtutem.
														Hic autem velut hereditate relictum odium paternum erga Romanos sic conservavit, ut prius animam quam id deposuerit,
														qui quidem, cum patria pulsus esset et alienarum opum indigeret, numquam destiterit animo bellare cum Romanis.
													</font>
												</p>
											</td>
											<td></td>
											<td>
												<p>
													<font size="4" color="#F2F2F2">
														Hannibal, der Sohn Hamilcars, war Karthager. Wenn wahr ist, was niemand bezweifelt, dass das römische Volk alle Völker an Tapferkeit übertroffen hat,
														kann nicht geleugnet werden, dass Hannibal den übrigen Feldherren so sehr an Klugheit überlegen war, wie das römische Volk den anderen Völkern an Tüchtigkeit vorsteht.
														Denn sooft man mit ihm in Italien kämpfte, ging er immer als Sieger hervor.
														Wenn er nicht in seiner Heimat durch den Neid seiner Mitbürger geschwächt worden wäre, scheint es,
														als hätte er die Römer besiegen können.
														Aber die Missgunst vieler besiegte die Tüchtigkeit eines Einzelnen.
														Dieser aber bewahrte den väterlichen Hass, der ihm wie ein Erbe hinterlassen worden war, so sehr,
														dass er eher sein Leben als diesen Hass abgelegt hätte; und der sogar, als er aus seiner Heimat vertrieben worden war  
														und Hilfe fremder Menschen bedurfte, niemals davon abließ, in seinem Herzen mit den Römern Krieg zu führen.
													</font>
												</p>
											</td>
