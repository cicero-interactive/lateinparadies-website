														Die Senatoren, die glaubten, sie würden niemals ohne Angst vor Hinterhalten sein,
														solange Hannibal lebte, schickten Gesandte nach Bithynien, unter ihnen Flamininus,
														die vom König verlangen sollten, dass er nicht ihren größten Feind beherbergen und ihn ihnen ausliefern solle.
														Ihnen zu widersprechen wagte Prusias nicht; das aber lehnte er ab,
														dass von ihm verlangt wurde, dass etwas getan werde, was gegen das Gastrecht sei:
														Sie sollten ihn selbst ergreifen, wenn sie könnten. Sie würden leicht den Platz finden, wo jener sei.
														Hannibal hielt sich nämlich an einem Ort auf, in dem kleinen Schloss,
														das ihm vom König zum Geschenk gemacht worden war und das so gebaut war,
														dass es an allen Seiten des Gebäudes Ausgänge gab, vielleicht weil er fürchtete, dass sich ereignen würde, was nun geschah.
														Dorthin sind römische Gesandte gekommen und hatten mit einer großen Menge an Soldaten sein Haus umzingelt.
														Daraufhin meldete ein am Tor Ausschau haltender junger Sklave, dass mehr Bewaffnete als gewöhnlich erschienen seien.
														Hannibal befahl ihm, das er an allen Toren des Hauses herumgehen und ihm möglichst schnell melden sollte,
														ob er von allen Seiten belagert werde.
														Der Sklave meldete schnell, was war, nämlich, das alle Eingänge belagert waren.
														Hannibal spürte, dass das nicht zufällig geschehen war, sondern dass er gesucht wurde und
														dass er das Leben nicht läger behalten konnte.
														Damit er es aber nicht nach fremdem Urteil verliere, nahm er in Erinnerung an
														seine frühere Tapferkeit Gift, das er immer bei sich hatte.
													</font>
												</p>
