														So starb dieser überaus tapfere Mann, der viele verschiedene Aufgaben bewältigt hatte, im 70. Jahr.
														Unter welchen Konsuln er starb, ist unklar. Denn Atticus überliefert in seinen Annalen,
														dass er unter den Konsuln Marcus Claudius Marcellus und Quintus Fabius Labeo gestorben ist, aber Polybius schreibt,
														er sei unter den Konsuln Lucius Aemilius Paullus und Cnaeius Baebius Tamphilus gestorben,
														Sulpicius aber unter den Konsuln Blithus Publius Cornelius Cethegus und Marcus Baebius Tamphilus.
														Aber dieser so bedeutende und in so vielen Kriegen beschäftigte Mann widmete einiges von seiner Zeit auch der Literatur.
														Denn es gibt einige Bücher von ihm, in griechischer Sprache verfasst,
														darunter eines an die Rhodier über die Taten des Cnaeus Manlius Volso in Asien.
														Hannibals Kriegstaten haben viele überliefert, von denen sogar zwei in seinem Lager waren und
														zur selben Zeit gelebt haben, solange es das Schicksal zuließ, nämlich Silenus und der Spartaner Sosylus.
