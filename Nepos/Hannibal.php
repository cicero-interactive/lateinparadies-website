													<font size="4" color="#F2F2F2">
														Cuius post obitum, Hasdrubale imperatore suffecto, equitatui omni praefuit. Hoc quoque interfecto exercitus summam imperii ad eum detulit.
														Id Carthaginem delatum publice comprobatum est.
														Sic Hannibal, minor V et XX annis natus imperator factus, proximo triennio omnes gentes Hispaniae bello subegit;
														Saguntum, foederatam civitatem, vi expugnavit; tres exercitus maximos comparavit.
														Ex his unum in Africam misit, alterum cum Hasdrubale fratre in Hispania reliquit,
														tertium in Italiam secum duxit. Saltum Pyrenaeum transiit. Quacumque iter fecit, cum omnibus incolis conflixit: neminem nisi victum dimisit.
														Ad Alpes posteaquam venit, quae Italiam ab Gallia seiungunt, quas nemo umquam cum exercitu ante eum praeter Herculem Graium transierat,
														quo facto is hodie saltus Graius appellatur, Alpicos conantes prohibere transitu concidit; loca patefecit, itinera muniit, effecit, ut ea elephantus ornatus ire posset,
														qua antea unus homo inermis vix poterat repere. Hac copias traduxit in Italiamque pervenit.
													</font>
												</p>
											</td>
											<td></td>
											<td>
												<p>
													<font size="4" color="#F2F2F2">
														In diesem Alter, von dem wir gesprochen haben, brach er mit dem Vater nach Spanien auf,
														nach dessen Tod er der gesamten Reiterschaft vorstand, nachdem Hasdrubal zum Feldherrn gemacht worden war.
														Nachdem auch er getötet worden war, übertrug das Heer den Oberbefehl auf ihn.
														Das wurde nach Karthago gemeldet und öffentlich gebilligt.
														So wurde Hannibal, der weniger als 25 Jahre alt war, zum Feldherrn gemacht.
														Im Zeitraum der nächsten drei Jahre unterwarf er alle Stämme Spaniens im Krieg;
														Sagunt, eine <font color="#CCCCCC">[mit uns (den Römern)]</font> verbündete Bürgerschaft, eroberte er mit Gewalt und schuf drei sehr große Heere.
														Von diesen schickte er eines nach Afrika, das zweite ließ er mit seinem Bruder Hasdrubal in Spanien zurück
														und das dritte führte er mit sich nach Italien. Er überquerte den Pass der Pyrenäen.
														Wo auch immer er seinen Marsch machte, führte er mit allen Bewohnern Krieg:
														Niemanden ließ er gehen, wenn er ihn nicht besiegt hatte.
														Als er zu den Alpen gekommen war, die Italien von Gallien trennen
														und welche niemand jemals mit einem Heer vor ihm überschritten hatte außer dem Griechen Herkules
														- aus diesem Grund wird der Pass "Der Griechische" genannt -
														tötete er die Alpenbewohner, weil sie versuchten, ihn am Durchmarsch zu hindern.
														Er eröffnete das Gelände, sicherte Wege und ermöglichte es, dass auf dem Weg ein Elefant in Kriegsrüstung gehen konnte,
														wo zuvor ein unbewaffneter Mann kaum hatte kriechen können. Auf diesem Weg führte er die Truppen und gelangte nach Italien.
													</font>
												</p>
