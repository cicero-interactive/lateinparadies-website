												<p>
													<font size="4" color="#F2F2F2">
														Hinc invictus patriam defensum revocatus bellum gessit adversus P. Scipionem, filium eius, quem ipse primo apud Rhodanum, iterum apud Padum,
														tertio apud Trebiam fugarat.
														Cum hoc exhaustis iam patriae facultatibus cupivit impraesentiarum bellum componere, quo valentior postea congrederetur.
														In colloquium convenit; condiciones non convenerunt.
														Post id factum paucis diebus apud Zamam cum eodem conflixit: pulsus - incredibile dictu - biduo et duabus noctibus Hadrumetum pervenit,
														quod abest ab Zama circiter milia passuum trecenta.
														In hac fuga Numidae, qui simul cum eo ex acie excesserant, insidiati sunt ei; quos non solum effugit, sed etiam ipsos oppressit.
														Hadrumeti reliquos e fuga collegit; novis dilectibus paucis diebus multos contraxit.
													</font>
												</p>
											</td>
											<td></td>
											<td>
												<p>
													<font size="4" color="#F2F2F2">
														Von hier wirde er als Unbesiegter zurückgerufen, um die Heimat zu verteidigen.
														Er führte Krieg gegen Publius Scipio, den Sohn des Scipio, den er selbst zum ersten Mal bei der Rhone,
														das zweite Mal beim Po und zum dritten Mal an der Trebia in die Flucht geschlagen hatte.
														Mit diesem wünschte er, weil die Möglichkeiten der Vaterstadt schon erschöpft waren, vorläufig den Krieg beizulegen,
														um später umso stärker zu käpfen. Es kam zu einer Unterredung, sie einigten sich aber nicht über die Bedingungen.
														Wenige Tage danach traf er bei Zama mit demselben Scipio zusammen;
