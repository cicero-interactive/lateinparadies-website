												<p>
													<font size="4" color="#F2F2F2">
														An der Rhone war er mit Publius Cornelius Scipio zusammengestoßen und hatte ihn vertrieben.
														Mit demselben kämpfte er bei Clastidium beim Po entscheidend und als Verwundeten schlug er ihn von dort
														in die Flucht.
														Derselbe Scipio stand ihm zum dritten Mal mit dessen Kollegen Tiberius Longus an der Trebia als Feind gegenüber.
														Mit ihnen kam es zum Kampf und er überwältigte beide.
														Von dort zog er durch das Gebiet der Ligurer über den Appennin und strebte Etrurien an.
														Auf diesem Marsch wurde er von einem schweren Augenleiden befallen,
														sodass er später sein rechtes nicht mehr gut benutzen konnte.
														Obwohl er auch damals durch seine Krankheit behindert und auf einer Tragbahre getragen wurde,
														tötete er den beim Trasumener See mit seinem Heer durch einen Hinterhalt umzingelten Gaius Flaminius,
														und wenig später auch den Prätor Gaius Centenius, der mit einer ausgesuchten Schar die Pässe besetzt hielt.
														Von hier gelangte er nach Apulien.
														Dort kamen ihm zwei Konsuln entgegen, Gaius Terentius und Lucius Aemilius.
														Die Heere beider schlug er in einer Schlacht, den Konsul Paulus tötete er und außerdem einige ehemalige Konsuln,
														unter ihnen Gnaeus Servilius Geminus, der im vorigen Jahr Konsul gewesen war.
													</font>
												</p>
