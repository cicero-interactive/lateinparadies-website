												<p>
													<font size="4" color="#F2F2F2">
														Nam ut omittam Philippum, quem absens hostem reddidit Romanis, omnium his temporibus potentissimus rex Antiochus fuit.
														Hunc tanta cupiditate incendit bellandi, ut usque a rubro mari arma conatus sit inferre Italiae.
														Ad quem cum legati venissent Romani, qui de eius voluntate explorarent darentque operam consiliis clandestinis ut Hannibalem in suspicionem regi adducerent,
														tamquam ab ipsis corruptus alia atque antea sentiret, neque id frustra fecissent idque Hannibal comperisset seque ab interioribus consiliis segregari vidisset,
														tempore dato adiit ad regem,
														eique cum multa de fide sua et odio in Romanos commemorasset, hoc adiunxit: 'Pater meus' inquit 'Hamilcar puerulo me, utpote non amplius VIIII annos nato,
														in Hispaniam imperator proficiscens Carthagine, Iovi optimo maximo hostias immolavit.
														Quae divina res dum conficiebatur, quaesivit a me, vellemne secum in castra proficisci. Id cum libenter accepissem atque ab eo petere coepissem,
														ne dubitaret ducere, tum ille 'Faciam', inquit 'si mihi fidem, quam postulo, dederis.' Simul me ad aram adduxit, apud quam sacrificare instituerat,
														eamque ceteris remotis tenentem iurare iussit numquam me in amicitia cum Romanis fore.
														Id ego ius iurandum patri datum usque ad hanc aetatem ita conservavi, ut nemini dubium esse debeat, quin reliquo tempore eadem mente sim futurus.
														Quare, si quid amice de Romanis cogitabis, non imprudenter feceris, si me celaris; cum quidem bellum parabis, te ipsum frustraberis, si non me in eo principem posueris.'
														Hac igitur, qua diximus, aetate cum patre in Hispaniam profectus est.
													</font>
												</p>
											</td>
											<td></td>
											<td>
												<p>
													<font size="4" color="#F2F2F2">
														Denn um von Philippos zu schweigen, den er, ohne mit ihm persönlich zusammenzutreffen,
														den Römern verfeindete, so war zu jener Zeit der mächtigste aller Könige Antiochos.
														Diesen entflammte er mit einer so großen Begier zu kämpfen,
														dass er vom Roten Meer aus versuchte, Italien den Krieg zu erklären.
														Römische Gesandte waren zu ihm gekommen. Diese sollten seine Absicht erkunden und
														durch geheime Ratschläge darauf achten, dass sie Hannibal beim König in Verdacht bringen.
														Er sollte eben, von diesen bestochen, eine andere Meinung über Hannibal haben als zuvor.
														Dies hatten sie nicht vergeblich gemacht.
														Aber als Hannibal davon erfahren und gesehen hatte,
														dass er von vertraulicheren Beratungen ausgeschlossen wurde,
														suchte er zu gegebener Zeit den König auf.
														Und nachdem er ihn ausführlich an seine Treue und seinen Hass gegen die Römer erinnert hatte, sagte er folgendes:
														"Mein Vater Hamilcar", sagte er, "brachte, als ich ein kleiner Junge war, nämlich nicht älter als neun Jahre,
														als er als Feldherr nach Spanien aufbrach, in Karthago dem Jupiter Optimus Maximus
														ein Tieropfer dar.
														Während diese göttliche Sache ausgeführt wurde, fragte er mich, ob ich mit ihm ins Lager aufbrechen wolle.
														Als ich dies gerne angenommen und zu betteln angefangen habe, damit er nicht zögern würde, mich mitzunehmen, sagte daraufhin jener:
														"Ich werde es tun, wenn du mir das treue Versprechen, das ich verlange, gegeben hast".
														Sofort führte er mich an den Altar heran, an dem er zu opfern begonnen hatte.
														Und er befahl mir zu schwören, wobei er sich an diesem festhielt,
														dass ich niemals mit den Römern Freundschaft schließen sollte.
														Ich habe diesen dem Vater geleisteten Eid bis in diese Zeit so bewahrt, sodass niemand bezweifeln muss,
														dass ich in der übrigen Zeit von derselben Gesinnung sein werde.
														Deshalb, wenn du irgendetwas freundliches gegenüber den Römern denkst,
														wirst du nicht unklug handeln, wenn du es vor mir verschweigst. Wenn du allerdings einen Krieg vorbereiten wirst,
														hintergehst du dich selbst, wenn du mich nicht darin zum obersten Feldherren machst".
													</font>
												</p>
