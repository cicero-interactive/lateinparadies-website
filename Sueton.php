  Gaius Suetonius Tranquillus war ein römischer Schriftsteller und Verwaltungsbeamter. Suetons bedeutendstes Werk sind die Kaiserviten, in denen er das Leben Caesars und der römischen Kaiser von Augustus bis Domitian schildert. Für die modernen Historiker liefert er mit seinen Schriften eine wertvolle Informationsquelle über das Leben römischer Gelehrter sowie der ersten römischen Kaiser, wenngleich seine Angaben teils mit Vorsicht zu behandeln sind, da Sueton vieles aus seinen Quellen allzu unkritisch übernahm.
