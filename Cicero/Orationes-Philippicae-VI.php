  - number: 4
    latin: >-
      Quamquam, Quirites, non est illa legatio, sed denuntiatio belli, nisi paruerit; ita enim est decretum, ut si legati ad Hannibalem mitterentur. Mittuntur enim, qui nuntient, ne oppugnet consulem designatum, ne Mutinam obsideat, ne provinciam depopuletur, ne dilectus habeat, sit in senatus populique Romani potestate. [...]
    german: >-
      Obwohl dies, Quiriten <span class="annotation">(Bürger)</span>, keine Gesandtschaft ist, sondern eine Kriegserklärung, falls er nicht gehorchen sollte. Denn so ist es beschlossen worden, als ob Gesandte zu Hannibal geschickt werden würden. Denn es werden Leute geschickt, um ihm anzuordnen, den gewählten Konsul nicht anzugreifen, Mutina nicht zu belagern, die Provinz nicht zu verwüsten und keine Truppen zu mobilisieren, sondern sich der Macht des Senats und des römischen Volkes zu unterwerfen. [...]
