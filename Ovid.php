description: >-
  Publius Ovidius Naso war ein antiker römischer Dichter. Er zählt in der römischen Literaturgeschichte neben Horaz und Vergil zu den drei großen Poeten der klassischen Epoche. Ovid schrieb in einer Frühphase Liebesgedichte, in einer mittleren Phase Sagenzyklen und in einer Spätphase Klagelieder. Ovids gut erhaltenes Werk übte, nachdem es in der Spätantike weniger beachtet worden war, einen immensen Einfluss auf die Dichtung, die bildende Kunst und die Musik des Mittelalters und des Barock aus. In der Romantik ging der Einfluss zurück, lebte im späteren 19. Jahrhundert aber wieder auf. Sein Werk hat sich in das kulturelle Gedächtnis der Nachwelt tief eingeprägt; hier ist vor allem sein Hauptwerk, die Metamorphosen, zu nennen.
