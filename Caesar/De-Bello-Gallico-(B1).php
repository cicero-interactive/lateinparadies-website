										<tr>
											<td>
												<p>
													<font size="4" color="#F2F2F2">
														Apud Helvetios longe nobilissimus fuit et ditissimus Orgetorix.
														Is M. Messala, [et P.] M. Pisone consulibus regni cupiditate inductus coniurationem nobilitatis fecit
														et civitati persuasit ut de finibus suis cum omnibus copiis exirent:
														perfacile esse, cum virtute omnibus praestarent, totius Galliae imperio potiri.
														Id hoc facilius iis persuasit, quod undique loci natura Helvetii continentur:
														una ex parte flumine Rheno latissimo atque altissimo, qui agrum Helvetium a Germanis dividit;
														altera ex parte monte Iura altissimo, qui est inter Sequanos et Helvetios;
														tertia lacu Lemanno et flumine Rhodano, qui provinciam nostram ab Helvetiis dividit.
														His rebus fiebat ut et minus late vagarentur et minus facile finitimis bellum inferre possent;
														qua ex parte homines bellandi cupidi magno dolore adficiebantur.
														Pro multitudine autem hominum et pro gloria belli atque fortitudinis angustos se fines habere arbitrabantur.
													</font>
												</p>
											</td>
											<td></td>
											<td>
												<p>
													<font size="4" color="#F2F2F2">
														Orgetorix war bei den Helvetiern bei weitem der Vornehmste und Reichste.
														Dieser zettelte eine Verschwörung des Adels im Konsulatsjahr des Messula und Piso an,
														weil er sich von der Gier nach Herrschaft verleiten ließ.
														Und er überzeugte seinen Stamm, dass dieser aus seinem Gebiet mit all seinen Truppen und Hab und Gut ausziehen sollte.
														Er sagte, dass es ganz leicht sei, sich der Herrschaft über ganz Gallien zu bemächtigen,
														weil sie alle anderen Stämme an Tapferkeit überträfen. Davon überzeugte er sie um so leichter,
														weil die Helvetier von allen Seiten durch die Natur des Ortes eingeschränkt waren:
														Auf der einen Seite durch den sehr breiten Rhein, die die Felder vom Gebiet der Germanen trennt,
														auf der anderen Seite durch das sehr hohe Juragebirge, das zwischen den Helvetiern und Sequanern liegt.
														Auf der dritten durch den Genfer See und die Rhone, die unsere <font color="#CCCCCC">(die römische)</font> Provinz von den Helvetiern trennt.
														Dadurch kam es, dass sie weniger herumwandern konnten und weniger leicht ihre Nachbarn bekriegen konnten.
														Deshalb wurde den Menschen, die so gierig nach Krieg waren, großer Schaden zugefügt.
														Sie glaubten, dass sie für die Anzahl an Menschen und für Kriegsruhm ein zu kleines Gebiet hätten.
													</font>
												</p>
											</td>
										</tr>
