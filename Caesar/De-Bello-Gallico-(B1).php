														Ea res est Helvetiis per indicium enuntiata. Moribus suis Orgetoricem ex vinculis causam dicere coegerunt;
														damnatum poenam sequi oportebat, ut igni cremaretur. Die constituta causae dictionis Orgetorix ad iudicium omnem suam familiam,
														ad hominum milia decem, undique coegit, et omnes clientes obaeratosque suos, quorum magnum numerum habebat,
