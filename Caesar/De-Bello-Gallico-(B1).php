											<td>
												<p>
													<font size="4" color="#F2F2F2">
														His rebus adducti et auctoritate Orgetorigis permoti constituerunt ea quae ad proficiscendum pertinerent comparare,
														iumentorum et carrorum quam maximum numerum coemere, sementes quam maximas facere,
														ut in itinere copia frumenti suppeteret, cum proximis civitatibus pacem et amicitiam confirmare.
														Ad eas res conficiendas biennium sibi satis esse duxerunt; in tertium annum profectionem lege confirmant.
														Ad eas res conficiendas Orgetorix deligitur. Is sibi legationem ad civitates suscipit.
														In eo itinere persuadet Castico, Catamantaloedis filio, Sequano,
														cuius pater regnum in Sequanis multos annos obtinuerat et a senatu populi Romani amicus appellatus erat,
														ut regnum in civitate sua occuparet, quod pater ante habuerit; itemque Dumnorigi Haeduo, fratri Diviciaci,
														qui eo tempore principatum in civitate obtinebat ac maxime plebi acceptus erat,
														ut idem conaretur persuadet eique filiam suam in matrimonium dat.
														Perfacile factu esse illis probat conata perficere, propterea quod ipse suae civitatis imperium obtenturus esset:
														non esse dubium quin totius Galliae plurimum Helvetii possent;
														se suis copiis suoque exercitu illis regna conciliaturum confirmat.
														Hac oratione adducti inter se fidem et ius iurandum dant et regno occupato per tres potentissimos ac firmissimos populos totius Galliae sese potiri posse sperant. 
													</font>
												</p>
											</td>
											<td></td>
											<td>
												<p>
													<font size="4" color="#F2F2F2">
														Hierdurch und durch das Ansehen des Orgetorix bewegt,
														beschlossen sie, das, was ihrer Meinung nach zu einem Auszug gehörte, vorzubereiten.
														Nämlich eine möglichst große Zahl Zugtiere und Karren anzukaufen; eine möglichst große Saat zu machen,
														damit unterwegs der Getreidevorrat genügt; und mit den benachbarten Stämmen Frieden und Freundschaft zu schließen.
														Sie glaubten, dass ihnen, um diese Dinge zu erledigen, ein Zeitraum von zwei Jahren genüge.
														Für das dritte Jahr legten sie per Gesetz den Aufbruch fest.
														Zur Ausführung dieser Maßnahmen wurde Orgetorix gewählt. Dieser nahm die Gesandtschaft zu den Stämmen auf sich.
														In dieser Reise überzeugte er Casticus, den Sohn des Catamantaloedes, eines Sequaners,
														sich in seinem Stamm des Thrones zu bemächtigen, den sein Vater vorher innegehabt hatte.
														Ebenso überredete er den Häduer Dumnorix, den Bruder des Diviacus,
														der in dieser Zeit die erste Stelle in seinem Stamm innehatte, und der beim Volk sehr angesehen war,
														Es sei sehr leicht zu tun, überzeugte er jene, das Versuchte durchzuführen,
														deswegen,weil er selbst in seinem Stamm die Herrschaft übernehmen werde.
														Und es sei nicht zweifelhaft, dass die Helvetier in ganz Gallien am meisten könnten.<br>
														Er bekräftigte, dass er mit seinen Truppen und seinem Heer die Herrschaft für jene <font color="#CCCCCC">(Dumnorix & Casticus)</font> gewinnen würde.
														Von dieser Rede veranlasst leisteten sie untereinander den Treueeid, und sie hofften,
														nachdem sie die Herrschaft übernommen hätten, dass sie mit Hilfe der drei mächtigen Völker
														ganz Gallien unterwerfen könnten.
													</font>
												</p>
											</td>
										</tr>
