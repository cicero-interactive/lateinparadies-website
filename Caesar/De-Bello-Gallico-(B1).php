												<p>
													<font size="4" color="#F2F2F2">
														Es gab insgesamt nur zwei Wege, auf denen die Helvetier ihre Heimat verlassen konnten, einen durch das Gebiet der Sequaner,
														schmal und beschwerlich, zwischen dem Jura und der Rhone, auf dem kaum einzelne Wägen fahren konnten;
														ein sehr hoher Berg aber hing herüber, sodass sehr wenige Leute ihn behindern hätten können.
														Der zweite Weg führte durch unsere Provinz, viel leichter und bequemer, weil zwischen dem Gebiet der Helvetier und dem der Allobroger
														- letztere wurden erst kürzlich "befriedet" - die Rhone fließt, die an einigen seichten Stellen überquert werden kann.
														Die aüßerste Stadt der Allobroger ist Genf, und sie liegt benachbart zum Gebiet der Helvetier. Aus dieser Stadt führt eine Brücke zu den Helvetiern.
														Die Helvetier gleubten, dass sie die Allobroger überzeugen oder sie mit Waffengewalt dazu zwingen könnten, sodass sie durch deren Gebiet ziehen dürften,
														weil diese noch nicht gut gegen das römische Volk gesinnt zu sein schienen. Nachdem alles zum Aufbruch vorbereitet war, setzen sie einen Tag fest,
														an dem sich alle an der Rhone treffen sollten. Dieser Tag war der 5. vor den Kalenden des Aprils im Konsulatsjahr des Lucius Piso und Aulus Gabinius <font color="#CCCCCC">(der 28. März 58 v. Chr.)</font>. 
													</font>
