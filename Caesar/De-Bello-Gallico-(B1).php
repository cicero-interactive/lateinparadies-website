											<li><font size="6" color="#FFCC00"><u>Kapitel 7</u></font></li>
										</ul>
									</div>
									<table class="trlChapter">
										<tr>
											<td>
												<p>
													<font size="4" color="#F2F2F2">
														Caesari cum id nuntiatum esset, eos per provincia nostram iter facere conari,
														maturat ab urbe proficisci et quam maximis potest itineribus in Galliam ulteriorem contendit et ad Genavam pervenit.
														Provinciae toti quam maximum potest militum numerum imperat (erat omnino in Gallia ulteriore legio una),
														pontem, qui erat ad Genavam, iubet rescindi. Ubi de eius aventu Helvetii certiores facti sunt,
														legatos ad eum mittunt nobilissimos civitatis, cuius legationis Nammeius et Verucloetius principem locum obtinebant,
														qui dicerent sibi esse in animo sine ullo maleficio iter per provinciam facere, propterea quod aliud iter haberent nullum:
														rogare ut eius voluntate id sibi facere liceat.
