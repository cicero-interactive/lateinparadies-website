												<p>
													<font size="4" color="#F2F2F2">
														Dieser Plan wurde bei den Helvetiern durch eine Anzeige gemeldet.
														Ihre Sitten zwangen Orgetorix, dass er sich als Gefangener vor dem Gericht verteidigen musste.
														Im Falle einer Verurteilung wäre er mit Verbrennung bestraft worden.
														Am festgesetzten Termin der Gerichtsverhandlung sammelte Orgetorix zur Urteilsverkündung von überall her seine ganze Familie,
														an die zehntausend Menschen (!), und führte am gleichen Ort seine Klienten und Schuldner zusammen, von denen er eine große Menge hatte.
														Durch diese befreite er sich davor, sich vor dem Gericht verantworten zu müssen.
														Als der Stamm, von dieser Sache erbitter, versuchte, sein Recht mit Waffengewalt durchzusetzen,
														und die Beamten eine große Menge Menschen aus dem Stammesgebiet versammelten, starb Orgetorix.
														Und es ist nicht klar / der Verdacht liegt nicht fern, dass er, wie die Helvetier glauben, Selbstmord begangen hat.
														
													</font>
