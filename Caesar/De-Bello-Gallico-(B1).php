														Sobald sie für diese Sache bereit zu sein glaubten, verbrannten sie alle ihre Städte, zwölf an der Zahl, an die vierhundert Dörfer, und die übrigen privaten Gebäude;
														sie verbrannten alle Getreidevorräte, außer die, die sie mitnehmen wollten, damit sie, wenn ihnen die Hoffnung auf Rückkehr in die Heimat genommen ist,
														desto entschlossener seien, alle Gefahren auf sich zu nehmen.
														Einen Mehlvorrat für drei Monate sollte sich jeder befehlsmäßig von zu Hause mitnehmen.
														Sie überredeten die Rauricer, Tulinger und Latobriger, ihre Grenznachbarn,
														dass sie den selben Plan benutzen sollten und nach Einäscherung ihrer Städte und Dörfer gemeinsam mit ihnen ausziehen sollten.
														Auch die Bojer, die jenseits des Rheins gewohnt hatten, nach Norikum gezogen waren und gerade Noreia angriffen,
														nahmen sie bei sich als Begleiter auf und schlossen sie sich an.
													</font>
