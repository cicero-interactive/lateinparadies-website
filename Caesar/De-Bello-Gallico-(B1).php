													<font size="4" color="#F2F2F2">
														Gallia est omnis divisa in partes tres, quarum unam incolunt Belgae, aliam Aquitani,
														tertiam qui ipsorum lingua Celtae, nostra Galli appellantur. Hi omnes lingua, institutis, legibus inter se differunt.
														Gallos ab Aquitanis Garumna flumen, a Belgis Matrona et Sequana dividit. Horum omnium fortissimi sunt Belgae,
														propterea quod a cultu atque humanitate provinciae longissime absunt,
														minimeque ad eos mercatores saepe commeant atque ea quae ad effeminandos animos pertinent important,
														proximique sunt Germanis, qui trans Rhenum incolunt, quibuscum continenter bellum gerunt.
														Qua de causa Helvetii quoque reliquos Gallos virtute praecedunt, quod fere cotidianis proeliis cum Germanis contendunt,
														cum aut suis finibus eos prohibent aut ipsi in eorum finibus bellum gerunt.
														[...]
													</font>
												</p>
											</td>
											<td></td>
											<td>
												<p>
													<font size="4" color="#F2F2F2">
														Gallien ist insgesamt in drei Teile aufgeteilt, von denen den einen die Belger bewohnen,
														den zweiten die Aquitanier und den dritten diejenigen, die in ihrer Sprache Kelten, in unserer Gallier genannt werden.
														Diese alle unterscheiden sich in ihren Sprachen, Einrichtungen und Gesetzen.
														Die Gallier trennt von den Aquitaniern der Fluss Garumna<font color="#FF9900" size="3"><sup>1</sup></font>, von den Belgern die Matrone<font color="#FF9900" size="3"><sup>2</sup></font> und die Sequana<font color="#FF9900" size="3"><sup>3</sup></font>.
														Von diesen allen sind die Belger die tapfersten. Denn sie sind am weitesten von unserer Kunst und Kultur entfernt.
														Selten kommen zu ihnen Kaufmänner, die Dinge importieren, die zur Verweichlichung des Geistes beitragen.
														Ihre Nachbarn sind die Germanen, die jenseits des Rheins siedeln und mit denen sie ununterbrochen Krieg führen.
														Deshalb übertreffen auch die Helvetier die übrigen Gallier an Tapferkeit,
														weil sie beinahe täglich in Kämpfen mit den Germanen verwickelt sind,
														wenn sie entweder diese von ihrem eigenen Stammesgebiet fernhalten oder selbst auf deren Gebiet Krieg führen. [...]
													</font>
												</p>
