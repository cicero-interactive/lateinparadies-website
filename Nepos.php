  Cornelius Nepos war ein römischer Geschichtsschreiber und Biograph. Nepos, der aus Oberitalien stammte und dem Ritterstand angehört haben dürfte, war befreundet mit dem neoterischen Dichter Catull, der ihm seine Gedichtsammlung widmete, Marcus Tullius Cicero und Titus Pomponius Atticus. Von seinen zahlreichen Werken (u. a. Geschichtsdarstellungen und eine Erdbeschreibung) sind uns nur einige Biographien aus De viris illustribus erhalten, nämlich die über die nichtrömischen Feldherren sowie die zum Buch der lateinischen Historiker gehörigen über Cato den Älteren und Atticus.
